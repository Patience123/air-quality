        <h1>四、PM10介绍</h1>
        <br>
        <div class="textdiv1">
            <p>可吸入颗粒物，通常是指粒径在10微米以下的颗粒物，又称PM10。可吸入颗粒物在环境空气中持续的时间很长，对人体健康和大气能见度的影响都很大。通常来自在未铺的沥青、水泥的路面上行驶的机动车、材料的破碎碾磨处理过程以及被风扬起的尘土。可吸入颗粒物被人吸入后，会积累在呼吸系统中，引发许多疾病，对人类危害大。
            </p>
            <p>  可吸入颗粒物的浓度以每立方米空气中可吸入颗粒物的毫克数表示。国家环保总局1996年颁布修订的《环境空气质量标准（GB3095-1996）》中将飘尘改称为可吸入颗粒物，作为正式大气环境质量标准。
            </p>
            <br>
            <h3>· PM10形成途径</h3>
            <br>
            <p>对燃烧直接产生的一次颗粒物可以分为细颗粒物和粗颗粒物，粗颗粒物通常由各种碎裂过程直接产生的直径大于6µm的粒子，而颗粒物主要通过汽化-凝结机理形成的。在燃烧过程中煤中的非主量元素会从焦炭颗粒内汽化，汽化的元素在炉膛内发生一系列化学动力学过程，形态也随之发生变化，随着温度的降低，一部分气相组分会发生均相成核，同时另一部分凝结到周围已存在的颗粒上，颗粒之间的碰撞引起凝聚生长成更大的颗粒，而处于粗颗粒模式的主要是矿物质，可以通过机械过程脱除，但是细颗粒物(如PM10和PM2.5)很容易穿过除尘设备，对带有高效的静电除尘器(ESP)或布袋除尘器可达到99.9%的除尘效率，排放的颗粒大部分可达到PM10，尽管大多数颗粒在更小的尺寸范围内，而湿式烟气脱硫装置(FGD)可以捕获更多的颗粒物(90%)颗粒物的排放可达到PM3.5，大部分是PM1对大多数的电站ESP排出的细颗粒物为PM3.5左右，而FGD只是PM1左右，所以燃煤电站与工业锅炉排放烟气中飞灰的中直径主要在1-10µm间，对废弃物焚烧也是如此。因此对燃烧过程来说，采用PM10和PM2.5来研究细微颗粒物的分布危害性要比过去的总悬浮颗粒物(TSP)更精确和有意义。
            </p>
            <p>PM2.5的来源复杂，成分自然也很复杂。主要成分是元素碳、有机碳化合物、硫酸盐、硝酸盐、铵盐。其它的常见的成分包括各种金属元素，既有钠、镁、钙、铝、铁等地壳中含量丰富的元素，也有铅、锌、砷、镉、铜等主要源自人类污染的重金属元素。
            </p>
            <p>可吸入颗粒物的形成主要有两个途径：其一，各种工业过程（燃煤、冶金、化工、内燃机等）直接排放的超细颗粒物；其二，大气中二次形成的超细颗粒物与气溶胶等。其中，第一种途径是可吸入颗粒物的主要形成源，也是可吸入颗粒物污染控制的重要对象。
            </p>
            <br>
            <h3>· PM10对健康的危害</h3>
            <br>
            <p>可吸入颗粒物被人吸入后，会累积在呼吸系统中，引发许多疾病。对粗颗粒物的暴露可侵害呼吸系统，诱发哮喘 病。 越细小的颗粒物对人体危害越大，粒径超过10微米的颗粒物可被鼻毛吸留，也可通过咳嗽排出人体，而粒径小于10微米的可吸入颗粒物可随人的呼吸沉积肺部，甚至可以进入肺泡、血液。在肺部沉积率最高的是粒径为1微米左右的颗粒物。这些颗粒物在肺泡上沉积下来，损伤肺泡和粘膜，引起肺组织的慢性纤维化，导致肺心病，加重哮喘病，引起慢性鼻咽炎、慢性支气管炎等一系列病变，严重的可 危及生命。颗粒物对儿童和老年人的危害尤为明显。
            </p>
            <p>细颗粒物对人体健康的危害要更大，因为直径越小，进入呼吸道的部位越深。10μm直径的颗粒物通常沉积在上呼吸道，2μm以下的可深入到细支气管和肺泡。细颗粒物进入人体到肺泡后，直接影响肺的通气功能，使机体容易处在缺氧状态。
            </p>
